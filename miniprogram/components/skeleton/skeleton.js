/**
 * ===========================================================================
 * skeleton 组件 — 骨架屏加载占位
 * ===========================================================================
 *
 * 骨架屏（Skeleton Screen）是一种加载状态的视觉占位符。
 * 在数据加载期间显示灰色矩形块，给用户"内容即将出现"的预期，
 * 比空白转圈加载体验更好。
 *
 * 使用方式：
 *   <skeleton wx:if="{{loading}}" count="{{4}}" />
 *
 * 参数：
 *   count: 显示的骨架块数量（默认 3，首页用 4 个占位）
 */

Component({
  properties: {
    // 骨架块数量，每个块包含两行（80%宽标题 + 40%宽副标题）
    count: {
      type: Number,
      value: 3
    }
  }
});
